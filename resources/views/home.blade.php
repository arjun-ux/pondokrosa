        const menuBtn = document.querySelector('.menu-btn');
        const navLinks = document.querySelector('.nav-links');

        menuBtn.addEventListener('click', () => {
            navLinks.classList.toggle('active');
        });

        // Close menu when clicking a link
        document.querySelectorAll('.nav-links a').forEach(link => {
            link.addEventListener('click', () => {
                navLinks.classList.remove('active');
            });
        });

        // Fade-in animation on scroll
        const fadeElements = document.querySelectorAll('.fade-in');

        const fadeInOnScroll = () => {
            fadeElements.forEach(element => {
                const elementTop = element.getBoundingClientRect().top;
                const elementBottom = element.getBoundingClientRect().bottom;

                if (elementTop < window.innerHeight && elementBottom > 0) {
                    element.classList.add('visible');
                }
            });
        };

        // Initial check
        fadeInOnScroll();

        // Check on scroll
        window.addEventListener('scroll', fadeInOnScroll);


        // Update Breadcrumbs based on scroll position
        const sections = document.querySelectorAll('section[id]');
        const breadcrumb = document.querySelector('.breadcrumb');

        const updateBreadcrumb = () => {
            let currentSection = '';

            sections.forEach(section => {
                const sectionTop = section.offsetTop - 100;
                const sectionHeight = section.offsetHeight;

                if (window.scrollY >= sectionTop && window.scrollY < sectionTop + sectionHeight) {
                    currentSection = section.id;
                }
            });

            // Update breadcrumb content
            if (currentSection) {
                const sectionTitle = document.querySelector(`#${currentSection} .section-title h2`)?.textContent ||
                                   document.querySelector(`#${currentSection} h2`)?.textContent ||
                                   currentSection.charAt(0).toUpperCase() + currentSection.slice(1);

                breadcrumb.innerHTML = `
                    <li class="breadcrumb-item"><a href="#"><i class="fas fa-home"></i> Beranda</a></li>
                    <li class="breadcrumb-item"><a href="#${currentSection}">${sectionTitle}</a></li>
                `;
            } else {
                breadcrumb.innerHTML = `
                    <li class="breadcrumb-item"><a href="#"><i class="fas fa-home"></i> Beranda</a></li>
                `;
            }
        };

        // Update breadcrumb on scroll
        window.addEventListener('scroll', updateBreadcrumb);
        // Initial update
        updateBreadcrumb();

        // FAQ Functionality
        const faqItems = document.querySelectorAll('.faq-item');

        faqItems.forEach(item => {
            const question = item.querySelector('.faq-question');

            question.addEventListener('click', () => {
                // Close all other items
                faqItems.forEach(otherItem => {
                    if (otherItem !== item) {
                        otherItem.classList.remove('active');
                    }
                });

                // Toggle current item
                item.classList.toggle('active');
            });
        });

        // Lazy Loading for Images
        document.addEventListener('DOMContentLoaded', function() {
            const lazyImages = document.querySelectorAll('img[data-src]');

            const imageObserver = new IntersectionObserver((entries, observer) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        const img = entry.target;
                        img.src = img.dataset.src;
                        img.removeAttribute('data-src');
                        observer.unobserve(img);
                    }
                });
            });

            lazyImages.forEach(img => imageObserver.observe(img));
        });
    </script>
